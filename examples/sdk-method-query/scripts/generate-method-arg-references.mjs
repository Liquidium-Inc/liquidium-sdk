function formatReferencesModule(methodReferences) {
  const entries = Object.entries(methodReferences)
    .map(([methodId, reference]) => {
      return `  ${createStringLiteral(methodId)}:\n    ${createStringLiteral(reference)},`;
    })
    .join("\n");

  return `// Generated by scripts/generate-method-arg-references.mjs. Do not edit by hand.\n\nexport const METHOD_ARG_REFERENCES: Record<string, string> = {\n${entries}\n};\n`;
}

function createStringLiteral(value) {
  const singleQuoteCount = countOccurrences(value, "'");
  const doubleQuoteCount = countOccurrences(value, '"');
  const quote = singleQuoteCount < doubleQuoteCount ? "'" : '"';

  return `${quote}${escapeStringLiteralValue(value, quote)}${quote}`;
}

function countOccurrences(value, searchValue) {
  return value.split(searchValue).length - 1;
}

function escapeStringLiteralValue(value, quote) {
  return value
    .replace(/\\/g, "\\\\")
    .replace(/\r/g, "\\r")
    .replace(/\n/g, "\\n")
    .replace(/\t/g, "\\t")
    .replace(/\f/g, "\\f")
    .split(String.fromCharCode(8))
    .join("\\b")
    .replace(new RegExp(quote, "g"), `\\${quote}`);
}

function createMethodReference(target) {
  const [moduleName, methodName] = target.split(".");
  const moduleSymbol = clientType.getProperty(moduleName);

  if (!moduleSymbol) {
    throw new Error(`Unable to find client.${moduleName}`);
  }

  const moduleType = checker.getTypeOfSymbolAtLocation(
    moduleSymbol,
    clientClass
  );
  const methodSymbol = moduleType.getProperty(methodName);

  if (!methodSymbol) {
    throw new Error(`Unable to find client.${target}`);
  }

  const methodDeclaration = methodSymbol.valueDeclaration;
  const methodType = checker.getTypeOfSymbolAtLocation(
    methodSymbol,
    methodDeclaration ?? clientClass
  );
  const signatures = checker.getSignaturesOfType(
    methodType,
    ts.SignatureKind.Call
  );
  const signature = signatures[0];

  if (!signature) {
    throw new Error(`Unable to read signature for client.${target}`);
  }

  const signatureText = createSignatureText({
    moduleName,
    methodName,
    signature,
    methodDeclaration,
  });
  const declarationTexts = collectReferencedDeclarations(methodDeclaration);

  if (declarationTexts.length === 0) {
    return `${signatureText}\n\nNo request object type.`;
  }

  return `${signatureText}\n\n${declarationTexts.join("\n\n")}`;
}

function createSignatureText({
  moduleName,
  methodName,
  signature,
  methodDeclaration,
}) {
  const parameterTexts = signature.getParameters().map((parameter) => {
    const declaration = parameter.valueDeclaration;
    const type = checker.getTypeOfSymbolAtLocation(
      parameter,
      declaration ?? methodDeclaration
    );
    const optionalMarker =
      declaration &&
      (ts.isParameter(declaration) || ts.isPropertySignature(declaration)) &&
      (declaration.questionToken || declaration.initializer)
        ? "?"
        : "";

    return `${parameter.getName()}${optionalMarker}: ${normalizeTypeText(
      checker.typeToString(type, declaration, ts.TypeFormatFlags.NoTruncation)
    )}`;
  });
  const returnType = normalizeTypeText(
    checker.typeToString(
      signature.getReturnType(),
      methodDeclaration,
      ts.TypeFormatFlags.NoTruncation
    )
  );

  return `client.${moduleName}.${methodName}(${parameterTexts.join(
    ", "
  )}): ${returnType}`;
}

function collectReferencedDeclarations(methodDeclaration) {
  if (!methodDeclaration) {
    return [];
  }

  const declarations = [];
  const queuedDeclarations = [];
  const seenDeclarationKeys = new Set();

  for (const parameter of methodDeclaration.parameters ?? []) {
    collectReferencedTypeDeclarations(parameter.type, queuedDeclarations);
  }

  while (queuedDeclarations.length > 0) {
    const declaration = queuedDeclarations.shift();
    if (!declaration) {
      continue;
    }

    const declarationKey = createDeclarationKey(declaration);
    if (seenDeclarationKeys.has(declarationKey)) {
      continue;
    }

    seenDeclarationKeys.add(declarationKey);
    declarations.push(formatDeclaration(declaration));
    collectReferencedTypeDeclarations(declaration, queuedDeclarations);
  }

  return declarations;
}

function collectReferencedTypeDeclarations(node, queuedDeclarations) {
  if (!node) {
    return;
  }

  if (ts.isTypeReferenceNode(node)) {
    queueTypeDeclaration(node.typeName, queuedDeclarations);
  }

  if (ts.isExpressionWithTypeArguments(node)) {
    queueTypeDeclaration(node.expression, queuedDeclarations);
  }

  ts.forEachChild(node, (child) =>
    collectReferencedTypeDeclarations(child, queuedDeclarations)
  );
}

function queueTypeDeclaration(nameNode, queuedDeclarations) {
  const declaration = getTypeDeclaration(nameNode);

  if (declaration) {
    queuedDeclarations.push(declaration);
  }
}

function getTypeDeclaration(nameNode) {
  let symbol = checker.getSymbolAtLocation(nameNode);

  if (!symbol) {
    return undefined;
  }

  if ((symbol.flags & ts.SymbolFlags.Alias) !== 0) {
    symbol = checker.getAliasedSymbol(symbol);
  }

  const declaration = symbol.declarations?.find(isTypeDeclaration);

  if (declaration?.name && OMITTED_DECLARATIONS.has(declaration.name.text)) {
    return undefined;
  }

  return declaration;
}

function isTypeDeclaration(declaration) {
  const sourceFile = declaration.getSourceFile();

  if (
    sourceFile.isDeclarationFile ||
    sourceFile.fileName.includes("node_modules")
  ) {
    return false;
  }

  return (
    ts.isInterfaceDeclaration(declaration) ||
    ts.isTypeAliasDeclaration(declaration)
  );
}

function createDeclarationKey(declaration) {
  return `${declaration.getSourceFile().fileName}:${declaration.pos}:${declaration.end}`;
}

function formatDeclaration(declaration) {
  if (ts.isTypeAliasDeclaration(declaration)) {
    const type = checker.getTypeFromTypeNode(declaration.type);

    if (
      type.isUnion() &&
      type.types.every((member) => member.isStringLiteral())
    ) {
      const members = type.types.map((member) => JSON.stringify(member.value));
      return `type ${declaration.name.text} = ${members.join(" | ")};`;
    }
  }

  const sourceText = declaration.getText();
  return normalizeTypeText(
    sourceText.replace(/^export\s+/, "").replace(/\nexport\s+/g, "\n")
  );
}

function normalizeTypeText(value) {
  return value
    .replace(/import\("[^"]+"\)\./g, "")
    .replace(/typeof Chain\.(BTC|ETH|ICP)/g, '"$1"')
    .replace(/typeof Asset\.(BTC|ETH|ICP|USDC|USDT)/g, '"$1"')
    .replace(/typeof SupplyPlanType\.(contractInteraction|transfer)/g, '"$1"');
}

function findClass(sourceFile, className) {
  const classDeclaration = findChild(sourceFile, (node) => {
    return ts.isClassDeclaration(node) && node.name?.text === className;
  });

  if (!classDeclaration) {
    throw new Error(`Unable to find class ${className}`);
  }

  return classDeclaration;
}

function findChild(node, predicate) {
  if (predicate(node)) {
    return node;
  }

  return ts.forEachChild(node, (child) => findChild(child, predicate));
}

function createDiagnosticHost() {
  return {
    getCanonicalFileName: (fileName) => fileName,
    getCurrentDirectory: () => process.cwd(),
    getNewLine: () => "\n",
  };
}
