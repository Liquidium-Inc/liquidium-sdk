const configFile = ts.readConfigFile(tsconfigPath, ts.sys.readFile);
if (configFile.error) {
  throw new Error(ts.formatDiagnostic(configFile.error, createDiagnosticHost()));
}

const parsedConfig = ts.parseJsonConfigFileContent(
  configFile.config,
  ts.sys,
  exampleDir
);
const program = ts.createProgram({
  rootNames: parsedConfig.fileNames,
  options: parsedConfig.options,
});
const checker = program.getTypeChecker();
const clientSource = program.getSourceFile(clientSourcePath);

if (!clientSource) {
  throw new Error(`Unable to load ${clientSourcePath}`);
}

const clientClass = findClass(clientSource, "LiquidiumClient");
const clientType = checker.getTypeAtLocation(clientClass.name ?? clientClass);
const references = Object.fromEntries(
  Object.entries(METHOD_REFERENCE_TARGETS).map(([methodId, target]) => {
    return [methodId, createMethodReference(target)];
  })
);

fs.writeFileSync(
  outputPath,
  `// Generated by scripts/generate-method-arg-references.mjs. Do not edit by hand.\n\nexport const METHOD_ARG_REFERENCES: Record<string, string> = ${JSON.stringify(
    references,
    null,
    2
  )};\n`
);

function createMethodReference(target) {
  const [moduleName, methodName] = target.split(".");
  const moduleSymbol = clientType.getProperty(moduleName);

  if (!moduleSymbol) {
    throw new Error(`Unable to find client.${moduleName}`);
  }

  const moduleType = checker.getTypeOfSymbolAtLocation(moduleSymbol, clientClass);
  const methodSymbol = moduleType.getProperty(methodName);

  if (!methodSymbol) {
    throw new Error(`Unable to find client.${target}`);
  }

  const methodDeclaration = methodSymbol.valueDeclaration;
  const methodType = checker.getTypeOfSymbolAtLocation(
    methodSymbol,
    methodDeclaration ?? clientClass
  );
  const signatures = checker.getSignaturesOfType(methodType, ts.SignatureKind.Call);
  const signature = signatures[0];

  if (!signature) {
    throw new Error(`Unable to read signature for client.${target}`);
  }

  const signatureText = createSignatureText({
    moduleName,
    methodName,
    signature,
    methodDeclaration,
  });
  const declarationTexts = collectReferencedDeclarations(methodDeclaration);

  if (declarationTexts.length === 0) {
    return `${signatureText}\n\nNo request object type.`;
  }

  return `${signatureText}\n\n${declarationTexts.join("\n\n")}`;
}

function createSignatureText({
  moduleName,
  methodName,
  signature,
  methodDeclaration,
}) {
  const parameterTexts = signature.getParameters().map((parameter) => {
    const declaration = parameter.valueDeclaration;
    const type = checker.getTypeOfSymbolAtLocation(
      parameter,
      declaration ?? methodDeclaration
    );
    const optionalMarker =
      declaration &&
      (ts.isParameter(declaration) || ts.isPropertySignature(declaration)) &&
      (declaration.questionToken || declaration.initializer)
        ? "?"
        : "";

    return `${parameter.getName()}${optionalMarker}: ${checker.typeToString(
      type,
      declaration,
      ts.TypeFormatFlags.NoTruncation
    )}`;
  });
  const returnType = checker.typeToString(
    signature.getReturnType(),
    methodDeclaration,
    ts.TypeFormatFlags.NoTruncation
  );

  return `client.${moduleName}.${methodName}(${parameterTexts.join(
    ", "
  )}): ${returnType}`;
}

function collectReferencedDeclarations(methodDeclaration) {
  if (!methodDeclaration) {
    return [];
  }

  const declarations = [];
  const queuedNames = [];
  const seenNames = new Set();

  for (const parameter of methodDeclaration.parameters ?? []) {
    collectTypeReferenceNames(parameter.type, queuedNames);
  }

  while (queuedNames.length > 0) {
    const name = queuedNames.shift();
    if (!name || seenNames.has(name)) {
      continue;
    }

    seenNames.add(name);

    const declaration = findTypeDeclaration(name);
    if (!declaration) {
      continue;
    }

    declarations.push(formatDeclaration(declaration));
    collectTypeReferenceNames(declaration, queuedNames);
  }

  return declarations;
}

function collectTypeReferenceNames(node, queuedNames) {
  if (!node) {
    return;
  }

  if (ts.isTypeReferenceNode(node) && ts.isIdentifier(node.typeName)) {
    queuedNames.push(node.typeName.text);
  }

  ts.forEachChild(node, (child) => collectTypeReferenceNames(child, queuedNames));
}

function findTypeDeclaration(name) {
  for (const sourceFile of program.getSourceFiles()) {
    if (sourceFile.isDeclarationFile || sourceFile.fileName.includes("node_modules")) {
      continue;
    }

    const declaration = findChild(sourceFile, (node) => {
      return (
        (ts.isInterfaceDeclaration(node) || ts.isTypeAliasDeclaration(node)) &&
        node.name.text === name
      );
    });

    if (declaration) {
      return declaration;
    }
  }

  return undefined;
}

function formatDeclaration(declaration) {
  const sourceText = declaration.getText();
  return sourceText
    .replace(/^export\s+/, "")
    .replace(/\nexport\s+/g, "\n")
    .replace(/\n/g, "\n");
}

function findClass(sourceFile, className) {
  const classDeclaration = findChild(sourceFile, (node) => {
    return ts.isClassDeclaration(node) && node.name?.text === className;
  });

  if (!classDeclaration) {
    throw new Error(`Unable to find class ${className}`);
  }

  return classDeclaration;
}

function findChild(node, predicate) {
  if (predicate(node)) {
    return node;
  }

  return ts.forEachChild(node, (child) => findChild(child, predicate));
}

function createDiagnosticHost() {
  return {
    getCanonicalFileName: (fileName) => fileName,
    getCurrentDirectory: () => process.cwd(),
    getNewLine: () => "\n",
  };
}
